package 연습장8.Member;

public class Board {
	
	public String id;
	public String title;
	public String content;
	public int count;
	
	
	
	public Board() {
	}

	
	
	public Board(String id, String title, String content, int count) {
		this.id = id;
		this.title = title;
		this.content = content;
		this.count = count;
	}
	
	
	
	
}
