package MVC연습장.model;

public class Member {
	public String id;
	public String pwd;
	public String name;
	public String phone;
	
	
	public Member() {
	}

	public Member(String id, String pwd, String name, String phone) {
		this.id = id;
		this.pwd = pwd;
		this.name = name;
		this.phone = phone;
	}
	
	
}
